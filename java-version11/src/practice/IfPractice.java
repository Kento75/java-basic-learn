package practice;

public class IfPractice {
    public static void main(String[] args) {
        int number = 9;

        if (number == 10) {
            System.out.println("10です");
        } else if (number == 9) {
            System.out.println("9です");
        } else {
            System.out.println("9でも10でもないです");
        }
    }
}
