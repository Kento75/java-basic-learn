package practice;

public class Variable {
    public static void main(String[] args) {
        String name = "Kento75";
        System.out.println("Hello " + name);

        // 文字列 String
        String number = "1" + "2";
        System.out.println(number);

        // プリミティブ型(整数） int
        int numberInt = 1 + 2;
        System.out.println(numberInt);

        // boolean true(真) or false(偽) の２値しか存在しない
        boolean flag = true;
        boolean flag2 = false;
    }
}
